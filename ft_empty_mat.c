#include "pipex.h"

void	ft_empty_mat(char **args)
{
	int	i;

	i = 0;
	if (args == ((void *)0))
		return ;
	while (args[i] != ((void *)0))
	{
		free(args[i]);
		i += 1;
	}
	if (args)
		free (args);
}
